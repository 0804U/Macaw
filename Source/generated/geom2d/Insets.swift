import Foundation

public class Insets {

	var top: Double = 0
	var right: Double = 0
	var bottom: Double = 0
	var left: Double = 0

	public init(top: Double = 0, right: Double = 0, bottom: Double = 0, left: Double = 0) {
		self.top = top	
		self.right = right	
		self.bottom = bottom	
		self.left = left	
	}

}
